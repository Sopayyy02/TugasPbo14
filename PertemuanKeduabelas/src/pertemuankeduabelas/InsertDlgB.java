    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        // TODO add your handling code here:
        if (tfIdb.getText().equals("") || tfJB.getText().equals("") || tfTT.getText().equals("") || tfIdp.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            int idb = Integer.parseInt(tfIdb.getText());
            String jb = tfJB.getText();
            int tt = Integer.parseInt(tfTT.getText());
            int idp = Integer.parseInt(tfIdp.getText());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PertemuanKeduabelasPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            Penerbit idPenerbit = em.find(Penerbit.class, idp);
            if (idPenerbit == null) {
                JOptionPane.showMessageDialog(null, "Penerbit dengan ID " + idp + " tidak ditemukan!");
                em.close();
                emf.close();
                return;
            }
            Buku b = new Buku();
            b.setIdBuku(idb);
            b.setJudul(jb);
            b.setTahunTerbit(tt);
            b.setIdPenerbit(idPenerbit); // ðŸ”— relasikan ke penerbit yang ditemukan
            em.persist(b);
            em.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Data buku berhasil disimpan!");
            hilang(); // bersihkan form
            menuutama.showTableBuku(); // refresh tabel
            em.close();
            emf.close();
        }

    }//GEN-LAST:event_BtnSaveActionPerformed

