    public void showTablePenerbit() {
        EntityManager em = Persistence.createEntityManagerFactory("PertemuanKeduabelasPU").createEntityManager();

        Query q = em.createNamedQuery("Penerbit.findAll");
        List<Penerbit> hasil = q.getResultList();

        model = new DefaultTableModel(new String[]{"Id Penerbit", "Nama Penerbit", "Alamat"}, 0);
        for (Penerbit data : hasil) {
            Object[] baris = new Object[3];
            baris[0] = data.getIdPenerbit();
            baris[1] = data.getNamaPenerbit();
            baris[2] = data.getAlamat();
            model.addRow(baris);
        }
        DtPenerbit.setModel(model);
    }

    public void showTableBuku() {
        EntityManager em = Persistence.createEntityManagerFactory("PertemuanKeduabelasPU").createEntityManager();
        Query q = em.createNamedQuery("Buku.findAll");
        List<Buku> hasil = q.getResultList();

        model = new DefaultTableModel(new String[]{"Id Buku", "Judul Buku", "Tahun Terbit", "Id Penerbit"}, 0);

        for (Buku data : hasil) {
            Object[] baris = new Object[4];
            baris[0] = data.getIdBuku();
            baris[1] = data.getJudul();
            baris[2] = data.getTahunTerbit();
            baris[3] = data.getIdPenerbit() != null ? data.getIdPenerbit().getIdPenerbit() : "-";
            model.addRow(baris);
        }

        DtBuku.setModel(model);
    }

