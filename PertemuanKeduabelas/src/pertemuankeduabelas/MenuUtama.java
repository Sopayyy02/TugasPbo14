    private void BttnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BttnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(this);

        if (returnValue != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File filePilihan = jfc.getSelectedFile();
        EntityManagerFactory emf = null;
        EntityManager em = null;

        try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
            String pemisah = "\\s*;\\s*"; // split yang toleran terhadap spasi sekitar ';'

            // BACA HEADER TERLEBIH DAHULU (jika file kosong, hentikan)
            String header = br.readLine();
            if (header == null) {
                JOptionPane.showMessageDialog(this, "File kosong.");
                return;
            }

            // buat factory & entity manager setelah memastikan file bisa dibaca
            emf = Persistence.createEntityManagerFactory("PertemuanKeduabelasPU");
            em = emf.createEntityManager();

            em.getTransaction().begin();
            String baris;
            int lineNo = 1; // header sudah terlewati

            while ((baris = br.readLine()) != null) {
                lineNo++;

                if (baris.trim().isEmpty()) {
                    continue;
                }

                // split tolerant: menghapus spasi di sekitar delimiter
                String[] data = baris.trim().split(pemisah);

                // cek minimal kolom (idb, judul, tahun, idp)
                if (data.length < 4) {
                    // bisa lewati atau tampil pesan; di sini kita lewati baris tidak valid
                    System.err.println("Baris " + lineNo + " di-skip: kolom tidak lengkap -> " + baris);
                    continue;
                }

                String idbStr = data[0].trim();
                String jb = data[1].trim();
                String ttStr = data[2].trim();
                String idpStr = data[3].trim();

                if (idbStr.isEmpty() || jb.isEmpty() || ttStr.isEmpty() || idpStr.isEmpty()) {
                    System.err.println("Baris " + lineNo + " di-skip: ada nilai kosong -> " + baris);
                    continue;
                }

                int idb, tt, idp;
                try {
                    idb = Integer.parseInt(idbStr);
                    tt = Integer.parseInt(ttStr);
                    idp = Integer.parseInt(idpStr);
                } catch (NumberFormatException nfe) {
                    System.err.println("Baris " + lineNo + " di-skip: format angka salah -> " + baris);
                    continue;
                }

                // cek penerbit ada
                Penerbit p = em.find(Penerbit.class, idp);
                if (p == null) {
                    System.err.println("Baris " + lineNo + " di-skip: penerbit tidak ditemukan -> ID " + idp);
                    continue;
                }

                // buat entity Buku dan persist
                Buku b = new Buku();
                b.setIdBuku(idb);
                b.setJudul(jb);
                b.setTahunTerbit(tt);
                b.setIdPenerbit(p);
                em.persist(b);
            }

            em.getTransaction().commit();
            JOptionPane.showMessageDialog(this, "Upload selesai dan berhasil!");
            showTableBuku();

        } catch (IOException ioEx) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Gagal membaca file: " + ioEx.getMessage());
        } catch (PersistenceException pex) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Id Buku tidak boleh duplicat");
        } catch (Exception ex) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Kesalahan saat upload: " + ex.getMessage());
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
            if (emf != null && emf.isOpen()) {
                emf.close();
            }
        }

    }//GEN-LAST:event_BttnUploadActionPerformed

