    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        if (tfUser.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi Terlebih Dahulu");
        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PertemuanKeduabelasPU");
            EntityManager em = emf.createEntityManager();
            String user = tfUser.getText();
            Akun y = null;
            try {
                y = em.createNamedQuery("Akun.findByUsername", Akun.class)
                        .setParameter("username", user)
                        .getSingleResult();
            } catch (Exception e) {
            }
            if (y != null) {
                // Tampilkan field-password
                lblPw.setVisible(true);
                tfPw.setVisible(true);
                btnGanti.setVisible(true);
                btnCari.setVisible(false);
                tfUser.setEditable(false);

                // üî• Buka dialog kata rahasia
                Kata_Rahasia dialog = new Kata_Rahasia(
                        (java.awt.Frame) SwingUtilities.getWindowAncestor(this),
                        true
                );
                dialog.setLocationRelativeTo(this);
                dialog.setVisible(true);

                String input = dialog.getKataRahasia();  // yang diketik user
                String rahasiaDB = y.getKataRhs();   // ambil dari database

                // üî• Cek apakah kata rahasia cocok
                if (input == null || !input.equalsIgnoreCase(rahasiaDB)) {

                    // Tampilkan dialog salah
                    JOptionPane.showMessageDialog(this,
                            "Jawaban Salah! \nTidak bisa mengganti Password",
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );

                    // Reset tampilan kembali
                    lblPw.setVisible(false);
                    tfPw.setVisible(false);
                    btnGanti.setVisible(false);
                    btnCari.setVisible(true);
                    tfUser.setEditable(true);

                    return; // hentikan proses
                }

                // Kalau benar ‚Üí fokus ke password
                tfPw.requestFocus();
            }

            em.close();
            emf.close();
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnGantiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGantiActionPerformed
        if (tfPw.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password baru harus diisi");
            return;
        }
        EntityManagerFactory emf = null;
        EntityManager em = null;
        try {
            emf = Persistence.createEntityManagerFactory("PertemuanKeduabelasPU");
            em = emf.createEntityManager();
            em.getTransaction().begin();
            String user = tfUser.getText().trim();
            String pw = tfPw.getText();
            Akun x = null;
            try {
                x = em.createNamedQuery("Akun.findByUsername", Akun.class)
                        .setParameter("username", user)
                        .getSingleResult();
            } catch (Exception ex) {
                x = null;
            }
            if (x == null) {
                JOptionPane.showMessageDialog(this, "Username tidak ditemukan saat update!");
            } else {
                x.setPassword(pw);
                em.merge(x);
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(this, "Password berhasil diubah");
                this.dispose();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi error saat mengubah password:\n" + ex.getMessage());
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }
    }//GEN-LAST:event_btnGantiActionPerformed

